# - Escribir el número 34 en binario, octal y hexadecimal.
num = 34
print("Binario de 34:", bin(num))
print("Octal de 34:", oct(num))
print("Hexadecimal de 34:", hex(num))

# - Escribir los números 0b1001, 0o12, 0xa3f en decimal.
print("Decimal de 0b1001:", 0b1001)
print("Decimal de 0o12:", 0o12)
print("Decimal de 0xa3f:", 0xa3f)
print("Decimal de 0xa3f:", 0xa3f)
